-- -------------------------------------------------------------------------------------------------
-- Remove the old mp3_albumart table.
-- -------------------------------------------------------------------------------------------------

DROP TABLE mp3_albumart;

-- -------------------------------------------------------------------------------------------------
-- Create a table to store art which has been extracted from media.
-- -------------------------------------------------------------------------------------------------

CREATE TABLE media_art (
  art_sha1     VARCHAR(40)  	NOT NULL,
  image        MEDIUMBLOB     	NOT NULL
  ,
  PRIMARY KEY (art_sha1)
  ) ENGINE=MyISAM;

-- -------------------------------------------------------------------------------------------------
-- Add art reference to media tables.
-- -------------------------------------------------------------------------------------------------

ALTER TABLE mp3s ADD art_sha1 VARCHAR(40);
ALTER TABLE movies ADD art_sha1 VARCHAR(40);

