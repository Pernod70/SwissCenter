  FOREIGN KEY (certificate)  REFERENCES certificates(cert_id)         ON DELETE SET NULL,
  KEY title    (title(50)),
  KEY artist   (artist(50)),
  KEY album    (album(50)),
  KEY genre    (genre(50)),
  KEY year     (year(50)),
  KEY dirname  (dirname(255)),
  KEY filename (dirname(255))
) TYPE=MyISAM;

