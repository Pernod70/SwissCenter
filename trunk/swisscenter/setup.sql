CREATE TABLE mp3s (
  file_id          int(10) unsigned not null auto_increment,
  dirname          text,
  filename         text,
  size int(11)     default null,
  length int(11)   default null,
  lengthstring     text,
  bitrate          text,
  version          text,
  title            text,
  artist           text,
  album            text,
  year             text,
  track            varchar(50) default null,
  genre            text,
  verified         char(1),
